def pascal(num):
    my_list = [[1] * x for x in range(num+2) if x != 0]

    for j in range(len(my_list)):
        for k in range(j+1):
            if j == k or k == 0:
                continue
            else:
                my_list[j][k] = my_list[j-1][k-1] + my_list[j-1][k]
    return my_list[num]

n = int(input())

print(pascal(n))
