def magicmatrix(matrix, n):
        count = [i for i in range(1, (n * n) + 1)]
        for i in range(n):
            for j in range(n):
                if matrix[i][j] == 0:
                    return 'NO'
                if matrix[i][j] not in count:
                    return 'NO'
                else:
                    count.remove(matrix[i][j])

