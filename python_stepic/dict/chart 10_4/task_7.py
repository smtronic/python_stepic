# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –¥–µ—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω–æ–µ —Å–ª–æ–≤–æ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ —á–∞—Å—Ç–æ—Ç—ã –±—É–∫–≤ –Ω–µ –ø–æ–≤—Ç–æ—Ä—è—é—Ç—Å—è.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# *!*!*?
# 3
# –∞: 3
# –Ω: 2
# —Å: 1
# Sample Output 1:

# –∞–Ω–∞–Ω–∞—Å

# –°—á–∏—Ç—ã–≤–∞–µ–º –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω–æ–µ —Å–ª–æ–≤–æ
s = input()

# –°—á–∏—Ç—ã–≤–∞–µ–º —á–∞—Å—Ç–æ—Ç—ã –±—É–∫–≤ –∏–∑ —Å–ª–æ–≤–∞—Ä—è –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –∏—Ö –≤ —Å–ª–æ–≤–∞—Ä–µ
dct = {int(b): a for a, b in (input().split(': ') for _ in range(int(input())))}

# –°–æ–∑–¥–∞–µ–º —Å–ª–æ–≤–∞—Ä—å —á–∞—Å—Ç–æ—Ç –±—É–∫–≤ –≤ –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω–æ–º —Å–ª–æ–≤–µ
word = {c: s.count(c) for c in s}

# –ó–∞–º–µ–Ω—è–µ–º –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–µ –±—É–∫–≤—ã –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ –∏–º –±—É–∫–≤—ã –∏–∑ —Å–ª–æ–≤–∞—Ä—è
print(''.join(dct[word[c]] for c in s))

