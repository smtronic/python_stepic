# –ø—Ä–æ–±–µ–ª—ã –∏ –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è .,!?:;-, –∫–æ—Ç–æ—Ä—ã–µ –Ω—É–∂–Ω–æ –∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞—Ç—å. –î—Ä—É–≥–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –Ω–µ—Ç.

from collections import Counter

string = input().lower().split()
string = [word.strip('.,!?:;-') for word in string]

# word_count = {word: string.count(word) for word in string}
word_count = Counter(string)

min_count = min(word_count.values())
words_with_min_count = sorted([word for word, count in word_count.items() if count == min_count])
print(words_with_min_count[0])
