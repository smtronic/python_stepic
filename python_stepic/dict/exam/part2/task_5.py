def merge(values):# values - —ç—Ç–æ —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π
    result = {}
    for dict in values:
        for key, value in dict.items():
            result[key] = result.get(key, set())
            result[key].add(value)
    return result
