# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø–æ–∫—É–ø–∞—Ç–µ–ª–µ–π –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ, –ø–æ—Å–ª–µ –∏–º–µ–Ω–∏ –∫–∞–∂–¥–æ–≥–æ –ø–æ–∫—É–ø–∞—Ç–µ–ª—è ‚Äî –¥–≤–æ–µ—Ç–æ—á–∏–µ, –∑–∞—Ç–µ–º —Å–ø–∏—Å–æ–∫ –Ω–∞–∑–≤–∞–Ω–∏–π –≤—Å–µ—Ö –ø—Ä–∏–æ–±—Ä–µ—Ç–µ–Ω–Ω—ã—Ö –∏–º —Ç–æ–≤–∞—Ä–æ–≤ –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ, –ø–æ—Å–ª–µ –Ω–∞–∑–≤–∞–Ω–∏—è –∫–∞–∂–¥–æ–≥–æ —Ç–æ–≤–∞—Ä–∞ ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –µ–¥–∏–Ω–∏—Ü —Ç–æ–≤–∞—Ä–∞. –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –∫–∞–∂–¥–æ–º —Ç–æ–≤–∞—Ä–µ –≤—ã–≤–æ–¥–∏—Ç—Å—è –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ –Ω–∞ –≤—Ç–æ—Ä–æ–π —Ç–µ—Å—Ç. –ï—Å–ª–∏ –ø–æ–∑–∏—Ü–∏–∏ —Ç–æ–≤–∞—Ä–æ–≤ –ø–æ–≤—Ç–æ—Ä—è—é—Ç—Å—è, —Ç–æ –≤ –∏—Ç–æ–≥–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ –ø–æ–ø–∞–¥–∞–µ—Ç —Å—É–º–º–∞—Ä–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ–≤–∞—Ä–∞ –ø–æ –¥–∞–Ω–Ω–æ–π –ø–æ–∑–∏—Ü–∏–∏.

# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# 5
# –†—É—Å–ª–∞–Ω –ü–∏—Ä–æ–≥ 1
# –¢–∏–º—É—Ä –ö–∞—Ä–∞–Ω–¥–∞—à 5
# –†—É—Å–ª–∞–Ω –õ–∏–Ω–µ–π–∫–∞ 2
# –¢–∏–º—É—Ä –¢–µ—Ç—Ä–∞–¥—å 12
# –†—É—Å–ª–∞–Ω –•–ª–µ–± 3
# Sample Output 1:

# –†—É—Å–ª–∞–Ω:
# –õ–∏–Ω–µ–π–∫–∞ 2
# –ü–∏—Ä–æ–≥ 1
# –•–ª–µ–± 3
# –¢–∏–º—É—Ä:
# –ö–∞—Ä–∞–Ω–¥–∞—à 5
# –¢–µ—Ç—Ä–∞–¥—å 12


n = int(input())

# –°–æ–∑–¥–∞–µ–º —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á–∏ ‚Äî –∏–º–µ–Ω–∞ –ø–æ–∫—É–ø–∞—Ç–µ–ª–µ–π, –∞ –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî —Å–ª–æ–≤–∞—Ä–∏ —Ç–æ–≤–∞—Ä–æ–≤ —Å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º
purchases = {}

for i in range(n):
    buyer, item, amount = input().split()
    amount = int(amount)

    if buyer not in purchases:
        purchases[buyer] = {}

    if item not in purchases[buyer]:
        purchases[buyer][item] = amount
    else:
        purchases[buyer][item] += amount

# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
for buyer in sorted(purchases.keys()):
    print(buyer + ':')
    for item in sorted(purchases[buyer].keys()):
        print(item, purchases[buyer][item])

# --------------------2 –í–ê–†–ò–ê–ù–¢---------------------------

result = {}
for _ in range(int(input())):
    client, item, count = input().split()
    result.setdefault(client, {item:0})
    result[client][item] = result[client].get(item, 0) + int(count)

for key, value in sorted(result.items()):
    print(f'{key}:')
    for key2, value2 in sorted(value.items()):
        print(f'{key2} {value2}')
