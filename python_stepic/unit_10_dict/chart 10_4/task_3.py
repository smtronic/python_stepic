# –¢–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ üü¢
# Sample Input 1:

# –í–∏–∂—É –∑–≤–µ—Ä–µ–π
# –ñ–∏–≤—É —Ä–µ–∑–≤–µ–π
# Sample Output 1:

# YES

word = input().lower().strip()
word2 = input().lower().strip()
my_dict  = {char: word.count(char)
            for char in word
            if char not in '  .,!?:;-'}

my_dict2  = {char: word2.count(char)
            for char in word2
            if char.isalpha()}

print('YES' if my_dict == my_dict2 else 'NO')
